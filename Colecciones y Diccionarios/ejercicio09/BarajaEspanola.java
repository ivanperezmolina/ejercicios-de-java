public class BarajaEspanola {

  //Atributos
  private String palo;
  private int numero;

  //Métodos
  //Constructor
  public BarajaEspanola(String palo, int numero) {
    this.palo = palo;
    this.numero = numero;
  }

  public String getPalo() {
    return palo;
  }

  public int getNumero() {
    return numero;
  }

  
}
