/**
 * Muestra los numeros multiples de 5 de 0 a 100 utilizando un bucle do-while
 *
 * @author Iván Pérez Molina
 */

public class Ejercicio03{
  public static void main(String[] args) {
        
    int i = 0;
    
    do {
      System.out.println(i);
      i=i+5;
    } while (i < 100);
  }
}
